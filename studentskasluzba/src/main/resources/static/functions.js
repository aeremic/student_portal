function showPredmete() {
    var x = document.getElementById("predmeti");
    if (x.style.display == "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function showIspite() {
    var x = document.getElementById("ispiti");
    if (x.style.display == "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

